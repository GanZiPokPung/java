
public class Number {

	public static void main(String[] args) {
		// Operator
		System.out.println(6 + 2); // 8
		System.out.println(6 - 2); // 4
		System.out.println(6 * 2); // 12
		System.out.println(6 / 2);
		
		System.out.println(Math.PI); // 3.141692653589793
		System.out.println(Math.floor(Math.PI)); // 3.0 (자름)
		System.out.println(Math.ceil(Math.PI)); // 4.0 (반올림)
	}

}
